#include <stdio.h>

char	*ft_strchr(const char *s, int c)
{
	char	ch;

	ch = (char)c;
	while (*s != '\0')
	{
		if (*s == ch)
			return ((char *)s);
		s++;
	}
	if (ch == '\0')
		return ((char *)s);
	return (NULL);
}

int	main(void)
{
	char	*p;

	p = ft_strchr("banana", 'a');
	if (p != NULL)
		printf("%s\n", p);
	return (0);
}
